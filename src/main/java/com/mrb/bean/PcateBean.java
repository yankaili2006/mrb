package com.mrb.bean;

public class PcateBean {
	
	
	private long cid;
	private String name;
	private long date;
	private long operid;
	
	/**
	 * @return the cid
	 */
	public long getCid() {
		return cid;
	}
	/**
	 * @param cid the cid to set
	 */
	public void setCid(long cid) {
		this.cid = cid;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the date
	 */
	public long getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(long date) {
		this.date = date;
	}
	/**
	 * @return the operid
	 */
	public long getOperid() {
		return operid;
	}
	/**
	 * @param operid the operid to set
	 */
	public void setOperid(long operid) {
		this.operid = operid;
	}
}
