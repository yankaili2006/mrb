package com.mrb.pbean;

public class UrlsBean {
	private UrlBean mp4;
	private UrlBean permanent;
	private UrlBean m3u8;

	public UrlBean getMp4() {
		return mp4;
	}

	public void setMp4(UrlBean mp4) {
		this.mp4 = mp4;
	}

	public UrlBean getPermanent() {
		return permanent;
	}

	public void setPermanent(UrlBean permanent) {
		this.permanent = permanent;
	}

	public UrlBean getM3u8() {
		return m3u8;
	}

	public void setM3u8(UrlBean m3u8) {
		this.m3u8 = m3u8;
	}

}
